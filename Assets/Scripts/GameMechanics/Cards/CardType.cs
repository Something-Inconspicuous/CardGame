
namespace GameMechanics.Cards {
    public enum CardType {
        ///<summary>Has no type. Should not be used with other types.</summary>
        NONE,
        ///<summary>Damage type cards deal any amount of damage to enemies.</summary>
        DAMAGE,
        ///<summary>Buff type cards give multipliers to damage and defense</summary>
        BUFF,
        DEBUFF
    }
}


